#arithmetic operations

print(10 + 3) # + is used for addition 
print(10 - 3) # - is used for subtraction
print(10 * 3) # * is used for multiplication
print(10 / 3) # / is used for division

print(10 // 3) # // is used for division and it returns int
print(10 ** 3) # ** is used for power function
print(10 % 3) # this is modulo operator used for remainder

