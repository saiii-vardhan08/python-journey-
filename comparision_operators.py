# means with that we can compare two entities
# we have >, >=, <, <=, ==, !=

x = 3 > 2 # this is the boolean expression means it tells weather x is true of false
print(x)

k = 3 >= 2
print(k)

a = 3 < 2
print(a)

b = 3 <= 2
